# Example 1: using the == operator to compare two values
a = 5
b = 10
result = (a == b)
print(result) # False

# Example 2: using the < operator to compare two values
c = 15
d = 20
result = (c < d)
print(result) # True

# Example 3: using the > operator to compare two values
c = 15
d = 20
result = (c > d)
print(result) # True

# Example 4: using the not keyword to negate a boolean value
k = True
result = not k
print(result) # False



