#=: simple assignment

x = 5  # x is now 5
print(x)


#+=: addition assignment

x = 5
x += 3  # x is now 8
print(x)


#-=: subtraction assignment

x = 5
x -= 3  # x is now 2
print(x)


#*=: multiplication assignment

x = 5
x *= 3  # x is now 15
print(x)


#/=: division assignment

x = 5
x /= 3  # x is now 1.6666666666666667
print(x)


#%=: modulus assignment

x = 5
x %= 3  # x is now 2
print(x)


#**=: exponentiation assignment

x = 5
x **= 3  # x is now 125
print(x)


#//=: floor division assignment

x = 5
x //= 3  # x is now 1
print(x)

