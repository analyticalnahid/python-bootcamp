#and: logical AND

x = 5
y = 3
z = 1

if x > y and x > z:
    print("x is the greatest")
else:
    print("x is not the greatest")

# Output: "x is the greatest"




#or: logical OR

x = 5
y = 3
z = 1

if x > y or x > z:
    print("x is greater than y or z")
else:
    print("x is not greater than y or z")

# Output: "x is greater than y or z"




#not: logical NOT

x = True

if not x:
    print("x is False")
else:
    print("x is True")

# Output: "x is True"

