""".........    Expression      .........."""

2 + 3   # this is an expression that produces the value 5

"hello" + "world"   # this is an expression that produces the value "helloworld"

len([1, 2, 3])   # this is an expression that calls the len() function and produces the value 3

x * y   # this is an expression that multiplies the values of x and y

max(a, b)   # this is an expression that calls the max() function and returns the maximum of a and b


""".........    Statement      .........."""


x = 2 + 3   # this is an assignment statement that binds the value 5 to the variable x

print("hello world")   # this is a function call statement that calls the print() function

if x > 5:   # this is an if statement
  print("x is greater than 5")

for i in range(10):   # this is a for loop statement
  print(i)

def greet(name):   # this is a function definition statement
  print("Hello, " + name)
